package com.revature.backend.model.api;

public class ApiFlagTemplate {
	String content;
	int id;
	String type;
	public ApiFlagTemplate() {
		super();
	}
	public ApiFlagTemplate(String content, int id, String type) {
		super();
		this.content = content;
		this.id = id;
		this.type = type;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((content == null) ? 0 : content.hashCode());
		result = prime * result + id;
		result = prime * result + ((type == null) ? 0 : type.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ApiFlagTemplate other = (ApiFlagTemplate) obj;
		if (content == null) {
			if (other.content != null)
				return false;
		} else if (!content.equals(other.content))
			return false;
		if (id != other.id)
			return false;
		if (type == null) {
			return other.type == null;
		} else return type.equals(other.type);
	}
	@Override
	public String toString() {
		return "ApiFlagTemplate [content=" + content + ", id=" + id + ", type=" + type + "]";
	}
	
	
	
	
}
